## [@bashly-upgrade completions send_completions]
send_completions() {
  echo $'# bamon completion                                         -*- shell-script -*-'
  echo $''
  echo $'# This bash completions script was generated by'
  echo $'# completely (https://github.com/bashly-framework/completely)'
  echo $'# Modifying it manually is not recommended'
  echo $''
  echo $'_bamon_completions_filter() {'
  echo $'  local words="$1"'
  echo $'  local cur=${COMP_WORDS[COMP_CWORD]}'
  echo $'  local result=()'
  echo $''
  echo $'  if [[ "${cur:0:1}" == "-" ]]; then'
  echo $'    echo "$words"'
  echo $''
  echo $'  else'
  echo $'    for word in $words; do'
  echo $'      [[ "${word:0:1}" != "-" ]] && result+=("$word")'
  echo $'    done'
  echo $''
  echo $'    echo "${result[*]}"'
  echo $''
  echo $'  fi'
  echo $'}'
  echo $''
  echo $'_bamon_completions() {'
  echo $'  local cur=${COMP_WORDS[COMP_CWORD]}'
  echo $'  local compwords=("${COMP_WORDS[@]:1:$COMP_CWORD-1}")'
  echo $'  local compline="${compwords[*]}"'
  echo $''
  echo $'  case "$compline" in'
  echo $'    *\'config validate\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_bamon_completions_filter "--help --verbose -h -v")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *\'config reset\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_bamon_completions_filter "--force --help -f -h")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *\'completions\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_bamon_completions_filter "--help -h")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *\'performance\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_bamon_completions_filter "--format --help --json --verbose -f -h -v")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *\'config edit\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_bamon_completions_filter "--editor --help -e -h")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *\'config show\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_bamon_completions_filter "--help --pretty -h -p")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *\'restart\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_bamon_completions_filter "--config --daemon --help -c -d -h")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *\'status\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_bamon_completions_filter "--failed-only --help --json --name --verbose -f -h -j -n -v")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *\'remove\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_bamon_completions_filter "--force --help -f -h")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *\'config\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_bamon_completions_filter "--help -h edit reset show validate")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *\'start\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_bamon_completions_filter "--config --daemon --help -c -d -h")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *\'stop\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_bamon_completions_filter "--force --help -f -h")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *\'list\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_bamon_completions_filter "--disabled-only --enabled-only --help -d -e -h")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *\'add\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_bamon_completions_filter "--command --description --disabled --enabled --help --interval -c -d -h -i")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *\'now\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_bamon_completions_filter "--async --help --name -a -h -n")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_bamon_completions_filter "--help --version -V -h -v add completions config list now performance remove restart start status stop")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'  esac'
  echo $'} &&'
  echo $'  complete -F _bamon_completions bamon'
  echo $''
  echo $'# ex: filetype=sh'
}